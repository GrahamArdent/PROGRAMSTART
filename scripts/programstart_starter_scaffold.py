def build_starter_scaffold_plan(project_name: str, recommendation: ProjectRecommendation) -> StarterScaffoldPlan:
    product_shape = recommendation.product_shape
    if product_shape == "cli tool":
        plan = build_cli_tool_plan(project_name, recommendation)
    elif product_shape == "api service":
        plan = build_api_service_plan(project_name, recommendation)
    elif product_shape == "web app":
        plan = build_web_app_plan(project_name, recommendation)
    elif product_shape == "mobile app":
        plan = build_mobile_app_plan(project_name, recommendation)
    elif product_shape == "data pipeline":
        plan = build_data_pipeline_plan(project_name, recommendation)
    else:
        plan = build_general_plan(project_name, recommendation)

    files = dict(plan.files)
    cli_property_test = build_cli_hypothesis_test(project_name, recommendation)
    if cli_property_test is not None:
        path, content = cli_property_test
        files[path] = content
    files.update(build_api_optional_files(project_name, recommendation))
    if plan.root_dir == "starter/web_app":
        files.update(build_web_optional_files(recommendation))
    if plan.root_dir == "starter/mobile_app":
        files.update(build_mobile_optional_files(recommendation))
    if any("dashboard" in s for s in recommendation.suggested_companion_surfaces):
        files.update(build_admin_dashboard_plan(project_name))
    env_example = service_env_example(project_name, recommendation, plan.root_dir)
    if env_example is not None:
        path, content = env_example
        files[path] = content
    api_example = api_env_example(project_name, recommendation, plan.root_dir)
    if api_example is not None:
        path, content = api_example
        existing = files.get(path, "")
        if existing:
            files[path] = existing.rstrip() + "\n\n" + content
        else:
            files[path] = content
    files[f"{plan.root_dir}/README.md"] = files.get(f"{plan.root_dir}/README.md", "")
    files["starter/README.md"] = starter_readme(project_name, recommendation, plan)
    return StarterScaffoldPlan(label=plan.label, root_dir=plan.root_dir, run_hint=plan.run_hint, files=files)


def write_starter_scaffold(destination_root: Path, plan: StarterScaffoldPlan) -> list[Path]:
    created: list[Path] = []
    for relative_path, content in sorted(plan.files.items()):
        path = destination_root / relative_path
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        created.append(path)
    return created
