@dataclass(slots=True)
class StarterScaffoldPlan:
    label: str
    root_dir: str
    run_hint: str
    files: dict[str, str]


def slugify_project_name(project_name: str) -> str:
    slug = re.sub(r"[^a-zA-Z0-9]+", "_", project_name.strip()).strip("_").lower()
    return slug or "generated_app"


def has_stack(recommendation: ProjectRecommendation, stack_name: str) -> bool:
    stack_names = {item.lower() for item in recommendation.stack_names}
    return stack_name.lower() in stack_names


def has_service(recommendation: ProjectRecommendation, service_name: str) -> bool:
    service_names = {item.lower() for item in recommendation.service_names}
    return service_name.lower() in service_names


def has_api(recommendation: ProjectRecommendation, api_name: str) -> bool:
    api_names = {item.lower() for item in recommendation.api_names}
    return api_name.lower() in api_names


def toml_string_list(items: list[str]) -> str:
    if not items:
        return "[]"
    lines = ["["]
    lines.extend(f'  "{item}",' for item in items)
    lines.append("]")
    return "\n".join(lines)


def starter_readme(project_name: str, recommendation: ProjectRecommendation, plan: StarterScaffoldPlan) -> str:
    stacks = ", ".join(recommendation.stack_names) if recommendation.stack_names else "No stack hints available"
    services = ", ".join(recommendation.service_names) if recommendation.service_names else "No external services inferred"
    return (
        f"# Starter Scaffold - {project_name}\n\n"
        "This scaffold is a runnable starting point generated by `programstart create`.\n\n"
        f"- Product shape: {recommendation.product_shape}\n"
        f"- Variant: {recommendation.variant}\n"
        f"- Archetype: {recommendation.archetype}\n"
        f"- Recommended stacks: {stacks}\n"
        f"- Inferred services: {services}\n"
        f"- Scaffold root: {plan.root_dir}\n"
        f"- Run hint: {plan.run_hint}\n\n"
        "Treat this as an implementation starter, not a source-of-truth planning document.\n"
    )


